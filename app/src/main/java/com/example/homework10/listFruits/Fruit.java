package com.example.homework10.listFruits;

public class Fruit {
    private final String name;
    private final int image;

    public Fruit(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }


    public int getImage() {
        return image;
    }


}
